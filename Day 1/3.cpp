#include <iostream>
using namespace std;

int main()
{
    int x; // declaration,  int is for integers
    x = 5; // initilization
    cout << x << endl;
    cout << x + 6 << endl;
    int y = 9; // both declaration and initilization in one line
    cout << x + y << endl;
    int z, c;
    z = 1;
    c = 2;
    cout << z + c << endl;
    // this is a single line comment, basically comments will be ignores, these are helpful for taking notes


    // updating my variable 
int p = 5;
cout << p << endl;   // here the value of p is 5
p = 8;   // we change the value of p - 5  to 8
cout << p << endl;   // printing the new variable, value 8


p = p + 7;  // this says , p wale dabe mai p + 7 ko dhal do,  8 + 7 ko p mai dhaldo

cout << p << endl;

    return 0;
}