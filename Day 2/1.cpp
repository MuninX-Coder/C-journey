#include <iostream>
using namespace std;

int main(){
    int x = 5;
    cout << x << endl;
    x += 2;
    cout << x << endl;
    x -= 8;
    cout << x << endl;
    x *= 10;
    cout << x << endl;

    // arithmetic operators 

    int y = 2, z = 5;
    cout << y + z << endl;  // (+) addition
    cout << y - z << endl;  // (-) subtraction
    cout << y * z << endl;  // (*)  multiplication
    cout << y / z << endl;  // (/)  division
    return 0;
}